# 5. Média de Notas
# Peça ao usuário para digitar três notas e calcule a média delas.

print('Sistema de Média Escolar.')
nota1 = float(input('Digite a nota da P1: '))
nota2 = float(input('Digite a nota da P2: '))
nota3 = float(input('Digite a nota da P3: '))

media = (nota1 + nota2 + nota3) / 3

print(f'Sua média este período é: {media}')
